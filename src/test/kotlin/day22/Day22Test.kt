package day22

import io.kotlintest.matchers.shouldBe
import io.kotlintest.specs.FeatureSpec

class Day22Test : FeatureSpec() {
    init {
        feature("day 22") {

            val example = ". . . . . . . . .\n" +
                    ". . . . . . . . .\n" +
                    ". . . . . . . . .\n" +
                    ". . . . . # . . .\n" +
                    ". . . # . . . . .\n" +
                    ". . . . . . . . .\n" +
                    ". . . . . . . . .\n" +
                    ". . . . . . . . ."

            val input = ".....###..#....#.#..##...\n" +
                    "......##.##...........##.\n" +
                    ".#..#..#.#.##.##.........\n" +
                    "...#..###..##.#.###.#.#.#\n" +
                    "##....#.##.#..####.####..\n" +
                    "#..##...#.##.##.....##..#\n" +
                    ".#.#......#...####...#.##\n" +
                    "###....#######...#####.#.\n" +
                    "##..#.####...#.#.##......\n" +
                    "##.....###....#.#..#.##.#\n" +
                    ".#..##.....#########.##..\n" +
                    "##...##.###..#.#..#.#...#\n" +
                    "...####..#...#.##.#..####\n" +
                    ".#..##......#####..#.###.\n" +
                    "...#.#.#..##...#####.....\n" +
                    "#..###.###.#.....#.#.###.\n" +
                    "##.##.#.#.##.#..#..######\n" +
                    "####.##..#.###.#...#..###\n" +
                    ".........#####.##.###..##\n" +
                    "..#.##.#..#..#...##..#...\n" +
                    "###.###.#.#..##...###....\n" +
                    "##..#.#.#.#.#.#.#...###..\n" +
                    "#..#.#.....#..#..#..##...\n" +
                    "........#######.#...#.#..\n" +
                    "..##.###.#.##.#.#.###..##"
            scenario("part1") {
                part1(example, 7) shouldBe 5
                part1(example, 70) shouldBe 41
                part1(example, 10000) shouldBe 5587

                part1(input, 10000) shouldBe 5538
            }
            scenario("part2") {

                part2(example, 100) shouldBe 26
                part2(example, 10000000) shouldBe 2511944
                part2(input, 10000000) shouldBe 2511090
            }
        }


    }
}